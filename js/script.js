let currentLang = 'en';
let typedPI = 0;
let typedCI = 0;
let typedDeleting = false;
let typedTimer = null;
const typedEl = document.getElementById('typed');

function startTyped() {
  clearTimeout(typedTimer);
  typedPI = 0;
  typedCI = 0;
  typedDeleting = false;
  typedEl.textContent = '';
  runTyped();
}

function runTyped() {
  const phrases = T[currentLang].hero_typed;
  const phrase = phrases[typedPI];
  if (!typedDeleting) {
    typedEl.textContent = phrase.slice(0, ++typedCI);
    if (typedCI === phrase.length) {
      typedDeleting = true;
      typedTimer = setTimeout(runTyped, 1800);
      return;
    }
  } else {
    typedEl.textContent = phrase.slice(0, --typedCI);
    if (typedCI === 0) {
      typedDeleting = false;
      typedPI = (typedPI + 1) % phrases.length;
    }
  }
  typedTimer = setTimeout(runTyped, typedDeleting ? 38 : 72);
}

function setLang(lang) {
  if (lang === currentLang) return;
  currentLang = lang;
  document.getElementById('btn-en').classList.toggle('active', lang === 'en');
  document.getElementById('btn-el').classList.toggle('active', lang === 'el');
  document.getElementById('btn-en').setAttribute('aria-pressed', lang === 'en');
  document.getElementById('btn-el').setAttribute('aria-pressed', lang === 'el');
  document.documentElement.lang = lang;

  const textEls = document.querySelectorAll('[data-i18n]');
  const htmlEls = document.querySelectorAll('[data-i18n-html]');
  const allEls = [...textEls, ...htmlEls];
  allEls.forEach((el) => el.classList.add('lang-fade'));

  setTimeout(() => {
    const dict = T[lang];
    textEls.forEach((el) => {
      const key = el.dataset.i18n;
      if (dict[key] !== undefined) el.textContent = dict[key];
    });
    htmlEls.forEach((el) => {
      const key = el.dataset.i18nHtml;
      if (dict[key] !== undefined) el.innerHTML = dict[key];
    });
    allEls.forEach((el) => el.classList.remove('lang-fade'));
    startTyped();
  }, 180);
}

const observer = new IntersectionObserver((entries) => {
  entries.forEach((entry) => {
    if (!entry.isIntersecting) return;
    const el = entry.target;
    const delay = parseInt(el.dataset.delay || 0, 10);
    setTimeout(() => {
      el.classList.add('visible');
      const fill = el.querySelector('.skill-fill');
      if (fill) setTimeout(() => { fill.style.width = `${fill.dataset.width}%`; }, 200);
    }, delay);
    observer.unobserve(el);
  });
}, { threshold: 0.1 });

document.querySelectorAll('.timeline-item').forEach((el, i) => {
  el.dataset.delay = i * 120;
  observer.observe(el);
});
document.querySelectorAll('.card, .achievement-card, .cert-item, .skill-item').forEach((el, i) => {
  el.dataset.delay = (i % 4) * 90;
  observer.observe(el);
});

startTyped();
